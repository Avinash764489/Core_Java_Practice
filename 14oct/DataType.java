//Declearation of class
class DataType{
	//main method
	public static void main(String arg[]){
		//delcearing local variable
		// integer variable
		int number1 = 98; 
		int number2 = 67;
		//calculating the sum
		System.out.println("Sum of "+number1+" and "+number2+" is "+(number1+number2)); 

		//boolean variable
		boolean status = true;
		System.out.println(status);

		//char variable
		char c = 'w';
		System.out.println(c);

		//float variable

		float f1 = 345.45f;
		System.out.println(f1);

		//double variable
		double d1 = 343.4653;
		System.out.println(d1);
	}
	//end of main
}
//end of class